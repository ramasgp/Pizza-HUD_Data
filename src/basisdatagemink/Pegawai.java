    public void search(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID Pegawai");
        tbl.addColumn("Nama Lengkap");
        tbl.addColumn("Username");
        tbl.addColumn("Password");
        
        try{
            String sql = "SELECT *  FROM Pegawai WHERE Id_pegawai like '%"+txtCari.getText()+"%' OR Fullname like '%"+txtCari.getText()+"%' OR User_name like '%"+txtCari.getText()+"%'";
            Connection con = (Connection)Connect.Connection();
            ResultSet as = con.createStatement().executeQuery(sql);
            
            while(as.next()){
                tbl.addRow(new Object[] {
                    as.getString("Id_pegawai"),
                    as.getString("Fullname"),
                    as.getString("User_name"),
                    as.getString("Pass_word")
                });
                tblPegawai.setModel(tbl);
            }
            
            JOptionPane.showMessageDialog(null, "Data berhasil ditemukan");

        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null,  e.getMessage());
        }
    }
    
