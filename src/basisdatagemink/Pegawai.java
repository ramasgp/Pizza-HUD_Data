    private void tblPegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPegawaiMouseClicked
        int baris = tblPegawai.rowAtPoint(evt.getPoint());
        String Id_pegawai = tblPegawai.getValueAt(baris, 0).toString();
        txtIdpegawai.setText(Id_pegawai);
        String Nama_lengkap = tblPegawai.getValueAt(baris, 1).toString();
        txtFullname.setText(Nama_lengkap);
        String User_name = tblPegawai.getValueAt(baris, 2).toString();
        txtUsername.setText(User_name);
        String Pass_word = tblPegawai.getValueAt(baris, 3).toString();
        txtPassword.setText(Pass_word);
    }//GEN-LAST:event_tblPegawaiMouseClicked

