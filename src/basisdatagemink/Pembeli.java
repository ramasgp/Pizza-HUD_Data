    private void tblPembeliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPembeliMouseClicked

        try{
            int baris = tblPembeli.rowAtPoint(evt.getPoint());
            String Id_pembeli = tblPembeli.getValueAt(baris, 0).toString();
            txtIdpembeli.setText(Id_pembeli);
            String Nama_lengkap = tblPembeli.getValueAt(baris, 1).toString();
            txtNama.setText(Nama_lengkap);
            String No_telp = tblPembeli.getValueAt(baris, 2).toString();
            txtNotelp.setText(No_telp);
            String Alamat = tblPembeli.getValueAt(baris, 3).toString();
            txtAlamat.setText(Alamat);
            String User_name = tblPembeli.getValueAt(baris, 4).toString();
            txtUsername.setText(User_name);
            String Pass_word = tblPembeli.getValueAt(baris, 5).toString();
            txtPassword.setText(Pass_word);
            Object Id_member = tblPembeli.getValueAt(baris, 6);
            if (Id_member != null){
                txtIdmember.setText((String) Id_member);
            }else{
                txtIdmember.setText(kosong);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,  e.getMessage());
        }    
        
        
    }//GEN-LAST:event_tblPembeliMouseClicked

