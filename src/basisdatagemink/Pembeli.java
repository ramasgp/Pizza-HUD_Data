    public void search(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID Pembeli");
        tbl.addColumn("Nama Lengkap");
        tbl.addColumn("No Telepon");
        tbl.addColumn("Alamat");
        tbl.addColumn("Username");
        tbl.addColumn("Password");
        tbl.addColumn("ID Member");
        
        try{
            String sql = "SELECT *  FROM Pembeli WHERE Id_pembeli like '%"+txtCari.getText()+"%' OR Nama like '%"+txtCari.getText()+"%' OR User_name like '%"+txtCari.getText()+"%' OR Id_member like '%"+txtCari.getText()+"%'";
            Connection con = (Connection)Connect.Connection();
            java.sql.ResultSet as = con.createStatement().executeQuery(sql);
            
            while(as.next()){
                tbl.addRow(new Object[] {
                    as.getString("Id_pembeli"),
                    as.getString("Nama"),
                    as.getString("Notelp"),
                    as.getString("Alamat"),
                    as.getString("User_name"),
                    as.getString("Pass_word"),
                    as.getString("Id_member")
                });
                tblPembeli.setModel(tbl);
            }
            
            JOptionPane.showMessageDialog(null, "Data berhasil ditemukan");

        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null,  e.getMessage());
        }
    }
    
